import java.util.*;
class Switchcase
{
public static void main(String[]args)
{
Scanner nam=new Scanner(System.in);
System.out.println("Enter name: ");
String name=nam.next();
switch(name)
{
	case "sownthi":
		System.out.println("sownthariya-RP12009");
	break;
	case "uma":
		System.out.println("uma-RP12011");
	break;
	case "meera": 
		System.out.println("meera-RP12018");
	break;
	default: System.out.println(" no name here");
}
}}







